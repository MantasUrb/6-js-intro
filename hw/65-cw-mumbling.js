function accum (s) {
    const newArr = [];
    for (i = 0; i < s.length; i++) {
        const uppLetter = s[i].toUpperCase();
        const otherLetters = s[i].toLowerCase().repeat(i);
        // console.log(otherLetters);
        newArr.push(uppLetter + otherLetters);
        // console.log(newArr);
    }
    return newArr.join('-');
}

